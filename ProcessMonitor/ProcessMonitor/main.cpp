/**
 * @file main.cpp
 * @brief Точка входа в приложение ProcessMonitor
 *
 * Файл содержит функцию main(), которая является
 * стартовой точкой выполнения программы.
 *
 * Здесь создаётся:
 * - объект QApplication (ядро Qt-приложения)
 * - главное окно MainWindow
 *
 * После инициализации запускается цикл обработки событий Qt.
 */

#include "MainWindow.h"

#include <QApplication>

 /**
  * @brief Главная функция приложения
  * @param argc Количество аргументов командной строки
  * @param argv Массив аргументов командной строки
  * @return Код завершения приложения
  *
  * Последовательность выполнения:
  * 1. Создание объекта QApplication
  * 2. Создание главного окна MainWindow
  * 3. Отображение окна
  * 4. Запуск event loop (цикла обработки событий)
  *
  * Цикл событий работает до закрытия окна пользователем.
  */
int main(int argc, char* argv[])
{
    // Инициализация Qt-приложения
    QApplication a(argc, argv);

    // Создание главного окна
    MainWindow w;

    // Отображение окна на экране
    w.show();

    // Запуск цикла обработки событий
    return a.exec();
}