/**
 * @file ProcessMonitor.h
 * @brief Заголовочный файл системы мониторинга процессов Windows
 *
 * Определяет класс ProcessMonitor,
 * предоставляющий набор статических функций
 * для взаимодействия с процессами операционной системы Windows.
 *
 * Основные возможности:
 * - поиск запущенных процессов
 * - запуск новых процессов
 * - завершение процессов
 * - проверка состояния процесса
 *
 * Реализация основана на WinAPI.
 */

#ifndef PROCESSMONITOR_H
#define PROCESSMONITOR_H

#include <QString>

 /**
  * @class ProcessMonitor
  * @brief Статический класс для работы с процессами Windows
  *
  * Класс предоставляет низкоуровневый интерфейс
  * для взаимодействия с процессами через WinAPI.
  *
  * Все методы являются статическими,
  * поэтому создание экземпляров класса не требуется.
  *
  * Используется компонентами:
  * - AppInfo
  * - MainWindow
  * - система мониторинга процессов
  */
class ProcessMonitor
{
public:

    /**
     * @brief Проверка наличия запущенного процесса
     * @param filePath Полный путь к исполняемому файлу
     * @param outPid Переменная для сохранения найденного PID
     * @return true — процесс найден, false — процесс не найден
     *
     * Алгоритм работы:
     * 1. Выполняется поиск процессов по имени executable-файла
     * 2. Проверяется полный путь процесса
     * 3. При совпадении возвращается PID найденного процесса
     *
     * Используется для:
     * - определения текущего состояния приложения
     * - предотвращения повторного запуска
     * - восстановления состояния после перезапуска программы
     */
    static bool isProcessRunning(const QString& filePath,
        int& outPid);

    /**
     * @brief Принудительное завершение процесса
     * @param pid Идентификатор процесса
     * @return true при успешном завершении
     *
     * Использует WinAPI-функцию TerminateProcess().
     *
     * Особенности:
     * - завершение выполняется немедленно
     * - приложение не получает возможности сохранить данные
     * - используется как аварийное завершение
     */
    static bool terminateProcess(int pid);

    /**
     * @brief Запуск нового процесса
     * @param filePath Путь к исполняемому файлу
     * @param arguments Аргументы командной строки
     * @param outPid Переменная для сохранения PID нового процесса
     * @return true при успешном запуске
     *
     * Использует WinAPI-функцию CreateProcessW().
     *
     * После успешного запуска:
     * - создаётся новый процесс
     * - возвращается PID процесса
     * - приложение начинает отслеживаться системой мониторинга
     */
    static bool launchProcess(const QString& filePath,
        const QString& arguments,
        int& outPid);

    /**
     * @brief Получение состояния процесса
     * @param pid Идентификатор процесса
     * @return Строковое описание состояния процесса
     *
     * Возможные результаты:
     * - "Running" — процесс отвечает
     * - "Not Responding" — процесс завис
     *
     * Проверка выполняется через SendMessageTimeout(),
     * отправляя сообщение главному окну процесса.
     */
    static QString getProcessStatus(int pid);
};

#endif // PROCESSMONITOR_H