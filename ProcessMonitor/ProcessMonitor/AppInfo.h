/**
 * @file AppInfo.h
 * @brief Заголовочный файл класса AppInfo
 *
 * Определяет класс AppInfo,
 * представляющий одно отслеживаемое приложение.
 *
 * Класс хранит:
 * - путь к исполняемому файлу
 * - аргументы запуска
 * - параметры автозапуска
 * - PID процесса
 * - текущее состояние приложения
 *
 * Также класс предоставляет методы
 * для управления жизненным циклом процесса:
 * запуск, остановка и мониторинг.
 */

#ifndef APPINFO_H
#define APPINFO_H

#include <QString>
#include <QTimer>

 /**
  * @class AppInfo
  * @brief Класс информации об отслеживаемом приложении
  *
  * Каждый объект AppInfo соответствует
  * одному приложению из пользовательского списка.
  *
  * Основные функции:
  * - хранение параметров запуска
  * - управление процессом
  * - отслеживание статуса
  * - поддержка автоперезапуска
  * - организация отложенного старта
  *
  * Используется компонентами:
  * - MainWindow
  * - ProcessMonitor
  * - Logger
  */
class AppInfo
{
public:

    /**
     * @brief Конструктор по умолчанию
     *
     * Создаёт пустой объект приложения
     * с начальными параметрами.
     */
    AppInfo();

    /**
     * @brief Конструктор с параметрами
     * @param filePath Полный путь к executable-файлу
     * @param arguments Аргументы командной строки
     * @param delaySeconds Задержка запуска в секундах
     *
     * Используется при:
     * - загрузке конфигурации
     * - добавлении нового приложения
     */
    AppInfo(const QString& filePath,
        const QString& arguments,
        int delaySeconds);

    /**
     * @brief Деструктор класса
     *
     * Освобождает ресурсы объекта,
     * включая таймер отложенного запуска.
     */
    ~AppInfo();

    /**
     * @brief Полный путь к исполняемому файлу
     *
     * Пример:
     * C:/Program Files/App/app.exe
     */
    QString filePath;

    /**
     * @brief Аргументы командной строки
     *
     * Передаются процессу при запуске.
     */
    QString arguments;

    /**
     * @brief Задержка автоматического запуска
     *
     * Измеряется в секундах.
     * Используется при старте приложения ProcessMonitor.
     */
    int delaySeconds;

    /**
     * @brief Флаг автоматического перезапуска
     *
     * Если true —
     * приложение будет автоматически перезапущено
     * после завершения или сбоя.
     */
    bool autoRestart;

    /**
     * @brief Идентификатор процесса
     *
     * PID = 0 означает,
     * что приложение не запущено.
     */
    int pid;

    /**
     * @brief Текущее состояние процесса
     *
     * Возможные значения:
     * - "Running"
     * - "Not Responding"
     * - "Stopped"
     */
    QString status;

    /**
     * @brief Таймер отложенного запуска
     *
     * Используется для запуска приложения
     * спустя delaySeconds после старта программы.
     */
    QTimer* delayTimer;

    /**
     * @brief Запуск приложения
     *
     * Выполняет создание нового процесса
     * через ProcessMonitor::launchProcess().
     *
     * При успешном запуске:
     * - обновляется PID
     * - меняется статус
     * - событие записывается в лог
     */
    void launch();

    /**
     * @brief Принудительное завершение процесса
     *
     * Завершает процесс по PID
     * через ProcessMonitor::terminateProcess().
     *
     * После завершения:
     * - pid сбрасывается
     * - статус становится "Stopped"
     * - autoRestart отключается
     */
    void terminate();

    /**
     * @brief Обновление состояния процесса
     *
     * Проверяет:
     * - существует ли процесс
     * - изменился ли PID
     * - отвечает ли процесс
     *
     * Метод вызывается системой мониторинга
     * каждые 2 секунды.
     */
    void updateStatus();

    /**
     * @brief Получение имени приложения
     * @return Имя файла без пути
     *
     * Пример:
     * C:/Apps/test.exe -> test.exe
     */
    QString displayName() const;
};

#endif // APPINFO_H