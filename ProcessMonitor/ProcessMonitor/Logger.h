/**
 * @file Logger.h
 * @brief Заголовочный файл системы логирования
 *
 * Определяет статический класс Logger,
 * предназначенный для ведения журнала событий приложения.
 *
 * Логирование используется для:
 * - фиксации запуска и завершения процессов
 * - записи ошибок
 * - отслеживания действий пользователя
 * - диагностики работы приложения
 *
 * Все записи сохраняются в файл:
 * monitor.log
 */

#ifndef LOGGER_H
#define LOGGER_H

#include <QString>

 /**
  * @class Logger
  * @brief Статический класс логирования приложения
  *
  * Класс предоставляет простой интерфейс
  * для записи сообщений в лог-файл.
  *
  * Особенности реализации:
  * - потокобезопасная запись
  * - автоматическое добавление временной метки
  * - единый лог-файл для всего приложения
  * - отсутствие необходимости создавать экземпляр класса
  *
  * Формат записи:
  * yyyy-MM-dd hh:mm:ss - сообщение
  *
  * Пример:
  * 2026-05-10 12:41:08 - Process started
  */
class Logger
{
public:

    /**
     * @brief Инициализация системы логирования
     *
     * Создаёт и открывает файл monitor.log
     * в режиме добавления данных.
     *
     * Если файл отсутствует,
     * он будет создан автоматически.
     *
     * В случае ошибки открытия
     * логирование отключается.
     */
    static void init();

    /**
     * @brief Завершение работы логгера
     *
     * Корректно закрывает лог-файл
     * и освобождает используемые ресурсы.
     *
     * Обычно вызывается при завершении приложения.
     */
    static void close();

    /**
     * @brief Запись сообщения в лог
     * @param message Текст сообщения
     *
     * Добавляет запись в monitor.log
     * с автоматической временной меткой.
     *
     * Метод является потокобезопасным
     * благодаря использованию мьютекса.
     */
    static void log(const QString& message);
};

#endif // LOGGER_H